def pregunta_09():
    """
    Retorne un diccionario que contenga la cantidad de registros en que
    aparece cada clave de la columna 5.

    Rta/
    {'aaa': 13,
     'bbb': 16,
     'ccc': 23,
     'ddd': 23,
     'eee': 15,
     'fff': 20,
     'ggg': 13,
     'hhh': 16,
     'iii': 18,
     'jjj': 18}}

    """

    with open('files/data.csv', 'r') as f:
        data = f.readlines()

    count = {}

    for i in data:
        key = i.split(',')[4].split(':')[0]
        if key in count:
            count[key] += 1
        else:
            count[key] = 1

    return count


